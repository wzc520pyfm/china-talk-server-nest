import { Column, CreateDateColumn, UpdateDateColumn, VersionColumn } from 'typeorm';
import { Mark } from '../enums/mark.enum';

export class ActionRecords {
  @CreateDateColumn({
    comment: '创建时间',
  })
  createTime: Date;

  @UpdateDateColumn({
    comment: '更新时间',
  })
  updateTime: Date;

  @VersionColumn({
    comment: '版本号: 记录更新次数',
  })
  version: any;

  @Column({
    type: 'enum',
    enum: Mark,
    default: Mark.NORMAL,
    comment: '标记',
  })
  mark: Mark;
}
